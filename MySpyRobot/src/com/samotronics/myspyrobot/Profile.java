package com.samotronics.myspyrobot;

public class Profile {

    String Name;
    String IpAddress;
    String Username;
    String Password;

    public Profile(String Name, String IpAddress, String Username, String Password) {
        this.Name = Name;
        this.IpAddress = IpAddress;
        this.Username = Username;
        this.Password = Password;
    }
}
